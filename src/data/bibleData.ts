export function getOffline50Questions(grade: 'low' | 'mid' | 'high', topic: string): any[] {
  const basePreset = bibleData[grade];
  const preBaked = [
    ...(basePreset.expectedQuestions || []),
    ...(basePreset.mockEvaluation || [])
  ].map((q, idx) => ({
    id: `pre-q-${idx + 1}`,
    type: q.type,
    question: q.question,
    options: q.options || (q.type === 'ox' ? ["O", "X"] : undefined),
    answer: q.answer,
    explanation: q.explanation
  }));

  const searchLower = topic.toLowerCase();
  let filtered = OFFLINE_BIBLE_POOL;

  if (searchLower.includes("창세") || searchLower.includes("아담") || searchLower.includes("홍수") || searchLower.includes("노아") || searchLower.includes("이삭") || grade === "low") {
    const gList = OFFLINE_BIBLE_POOL.filter((_, idx) => idx < 9 || idx >= 26);
    filtered = [...gList, ...OFFLINE_BIBLE_POOL];
  } else if (searchLower.includes("출애") || searchLower.includes("모세") || searchLower.includes("십계") || searchLower.includes("홍해") || searchLower.includes("만나") || grade === "mid") {
    const exList = OFFLINE_BIBLE_POOL.filter((_, idx) => idx >= 9 && idx < 18);
    filtered = [...exList, ...OFFLINE_BIBLE_POOL];
  } else if (searchLower.includes("다윗") || searchLower.includes("사울") || searchLower.includes("사무") || searchLower.includes("솔로") || searchLower.includes("엘리") || grade === "high") {
    const kList = OFFLINE_BIBLE_POOL.filter((_, idx) => idx >= 18 && idx < 26);
    filtered = [...kList, ...OFFLINE_BIBLE_POOL];
  }

  const synthesized = [...preBaked];
  let poolIdx = 0;

  while (synthesized.length < 50 && poolIdx < filtered.length * 2) {
    const src = filtered[poolIdx % filtered.length];
    const isAlreadyPresent = synthesized.some(q => q.question.includes(src.question) || src.question.includes(q.question));
    if (!isAlreadyPresent) {
      synthesized.push({
        id: `f-q-${synthesized.length + 1}`,
        type: src.type,
        question: `[실전: ${topic.slice(0, 8)}] ${src.question}`,
        options: src.options || (src.type === 'ox' ? ["O", "X"] : undefined),
        answer: src.answer,
        explanation: src.explanation
      });
    }
    poolIdx++;
  }

  let padIdx = 0;
  while (synthesized.length < 50) {
    const src = filtered[padIdx % filtered.length];
    synthesized.push({
      id: `f-q-${synthesized.length + 1}`,
      type: src.type,
      question: `[실력 다지기] ${src.question}`,
      options: src.options || (src.type === 'ox' ? ["O", "X"] : undefined),
      answer: src.answer,
      explanation: src.explanation
    });
    padIdx++;
  }

  return synthesized.slice(0, 50).map((q, idx) => ({
    ...q,
    id: `custom-q-${idx + 1}`
  }));
}

