export function generate100Questions(grade?: 'low' | 'mid' | 'high'): LessonQuestion[] {
  // Helper to shuffle an array
  const shuffle = <T>(arr: T[]): T[] => {
    return [...arr].sort(() => Math.random() - 0.5);
  };

  // If a specific grade is requested, handle it. Otherwise, shuffle all questions and take 100.
  let combined: LessonQuestion[] = [];
  if (grade) {
    const primarySet = bibleLessonsQuestions.filter(q => q.difficulty === grade);
    const fillerPool = bibleLessonsQuestions.filter(q => q.difficulty !== grade);
    const selectedFillers = shuffle(fillerPool).slice(0, 74);
    combined = [...primarySet, ...selectedFillers];
  } else {
    // Return a random 100 questions from all 104 available questions
    combined = shuffle(bibleLessonsQuestions).slice(0, 100);
  }

  const finalExam = shuffle(combined);

  // Return with customized sequential indices
  return finalExam.map((q, idx) => ({
    ...q,
    id: `exam-q-${idx + 1}`
  }));
}


